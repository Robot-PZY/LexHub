def main() -> None:
    for legacy in ROOT.glob("case-*"):
        if legacy.is_dir():
            shutil.rmtree(legacy)

    for case_id, files in CASES.items():
        case_dir = ROOT / case_id
        case_dir.mkdir(parents=True)
        for filename, title, body in files:
            save_pdf(case_dir / filename, title, body)
