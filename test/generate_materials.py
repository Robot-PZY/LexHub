"""Generate LexHub demo case materials (PDF). Run: python generate_materials.py"""
from __future__ import annotations

import shutil
import sys
from pathlib import Path

try:
    from fpdf import FPDF
except ImportError:
    import subprocess
    subprocess.check_call([sys.executable, "-m", "pip", "install", "fpdf2", "-q"])
    from fpdf import FPDF

ROOT = Path(__file__).resolve().parent

FONT_CANDIDATES = [
    Path(r"C:\Windows\Fonts\simhei.ttf"),
    Path(r"C:\Windows\Fonts\msyh.ttc"),
    Path(r"C:\Windows\Fonts\simsun.ttc"),
    Path("/usr/share/fonts/truetype/wqy/wqy-microhei.ttc"),
    Path("/System/Library/Fonts/PingFang.ttc"),
]


def resolve_font() -> Path:
    for path in FONT_CANDIDATES:
        if path.is_file():
            return path
    raise FileNotFoundError("No CJK font found. Install simhei.ttf or run on Windows.")


FONT_PATH = resolve_font()


class CnPDF(FPDF):
    def __init__(self) -> None:
        super().__init__()
        self.add_font("cn", "", str(FONT_PATH))
        self.set_font("cn", size=11)

    def write_block(self, text: str, line_h: float = 7) -> None:
        self.multi_cell(0, line_h, text)
        self.ln(2)


def save_pdf(path: Path, title: str, body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    pdf = CnPDF()
    pdf.add_page()
    pdf.set_font("cn", size=14)
    pdf.multi_cell(0, 9, title)
    pdf.ln(4)
    pdf.set_font("cn", size=11)
    pdf.write_block(body)
    pdf.output(str(path))


